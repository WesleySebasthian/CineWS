    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FilmeBO filmeBO = new FilmeBO();
        filme.setNome(txtNome.getText().trim());
        try {
            filme.setGenero((Genero) cmbGenero.getSelectedItem());
            filme.setAnolancamento(Integer.parseInt(txtLancamento.getText().trim()));
            filme.setResumo(txtResumo.getText().trim());
            filme.setDuracao(ConverteData.StringForTime(txtDuracao.getText().trim()));
            filme.setIniciocartaz(ConverteData.StringForDate(txtInicio.getText().trim()));
            filme.setFimcartaz(ConverteData.StringForDate(txtFim.getText().trim()));
            filmeBO.editar(filme);
            JOptionPane.showMessageDialog(this, "Alteração Realizado com Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ano de Lançamento Inválido!", "Erro!", JOptionPane.WARNING_MESSAGE);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Data e Hora Inválidos!", "Erro!", JOptionPane.WARNING_MESSAGE);
        } catch (CampoInvalidoException e) {
            JOptionPane.showMessageDialog(this, "Preencha Todos os Campos!", "Erro!", JOptionPane.WARNING_MESSAGE);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            FilmeBO filmeBO = new FilmeBO();
            filmeBO.deletar(filme);
            JOptionPane.showMessageDialog(this, "Exclusão Realizada com Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            FilmeForm filmeForm = new FilmeForm();
            filmeForm.setVisible(true);
            this.dispose();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


