    private void carregarCmbGenero() throws SQLException {
        GeneroBO generoBO = new GeneroBO();
        DefaultComboBoxModel model = (DefaultComboBoxModel) cmbGenero.getModel();
        model.removeAllElements();
        model.addElement(this.filme.getGenero());
        for (Genero genero : generoBO.buscarTodos()) {
            if (genero.getId() != this.filme.getGenero().getId()) {
                model.addElement(genero);
            }
        }
    }
