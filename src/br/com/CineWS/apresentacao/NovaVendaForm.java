    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VendaBO vendaBO = new VendaBO();
        Venda venda = new Venda();
        venda.setTipo_ingresso((String) cmbIngresso.getSelectedItem());
        venda.setSessao((Sessao) cmbSessao.getSelectedItem());
        try {
            vendaBO.cadastrar(venda);
            JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            carregarCmbSessao();
        } catch (VendaInvalidaException e) {
            JOptionPane.showMessageDialog(this, "Sessão Ultrapassou Lotação Maxima!", "Erro!", JOptionPane.WARNING_MESSAGE);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

