    private void carregarCmbSessao() throws SQLException {
        SessaoBO sessaoBO = new SessaoBO();
        DefaultComboBoxModel model = (DefaultComboBoxModel) cmbSessao.getModel();
        model.removeAllElements();
        for (Sessao sessao : sessaoBO.buscarTodos()) {
            model.addElement(sessao);
        }
    }
