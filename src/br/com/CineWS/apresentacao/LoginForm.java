    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        Funcionario funcionario = new Funcionario();
        funcionario.setLogin(txtLogin.getText().trim());
        funcionario.setSenha(txtSenha.getText().trim());
        FuncionarioBO funcionarioBO = new FuncionarioBO();
        try {
            funcionarioBO.login(funcionario);
            PrincipalForm principalForm = new PrincipalForm();
            principalForm.setVisible(true);
            this.dispose();
        } catch (CampoInvalidoException e) {
            JOptionPane.showMessageDialog(this, "Preencha Todos os Campos!", "Erro!", JOptionPane.WARNING_MESSAGE);
        } catch (LoginInvalidoException e) {
            JOptionPane.showMessageDialog(this, "Dados Inv√°lido!", "Erro!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

